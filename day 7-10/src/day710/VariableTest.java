package day710;

public class VariableTest {
	public static void main(String[] args) {
		int num; //변수 선언
		num = 10; //변수 초기화
		System.out.println(num); //num은 num에 저장된 값으로 생각.
		
		String name;
		name = "서의진";
		System.out.println(name); //단축키 ctrl + alt + 위/아래 방향키
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		System.out.println(name);
		
		double pi = 3.14; // 선언과 초기화를 동시
		
		System.out.println(pi);
		
		pi = 10.5;
		
		System.out.println(pi);
		
		//변수 선언시 변수명 메모리 안에 변수 값을 넣어준다고 생각하기. 수학 공식 처럼 변수값=변수명이 아님.
		
	}
}
